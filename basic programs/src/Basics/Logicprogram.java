package Basics;

public class Logicprogram {
	public static void main(String args[]) {
		int num = 20;
		int sum = 0;
		int output;
		System.out.println("The entered number is:" + num);
		int n = 2;
		for (int i = 1; i <= n; i++) {

			sum = (num + num);
			
			output = (sum) / 2;

			System.out.println("The final output is:" + output);
		}
	}
}
